#include "../philo_bonus.h"

void	mep_sem_str(t_philo *p)
{
	int	i;

	i = 0;
	while (i < p->nb)
	{
		p->str_sem[i][0] = 'I';
		p->str_sem[i][1] = '0' + (i + 1) / 100;
		p->str_sem[i][2] = '0' + ((i + 1) / 10 % 10);
		p->str_sem[i][3] = '0' + ((i + 1) % 10);
		p->str_sem[i][4] = 0;
		i++;
	}
}
